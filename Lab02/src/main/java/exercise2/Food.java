package exercise2;

public class Food {
	private String name;
	private double amountofSugar;
	
	public Food (String name, double amountofSugar) {
		this.name=name;
		this.amountofSugar=amountofSugar;
		
	}
	
	public String getName() {
		return this.name;
		
	}
	
	public double getSugarLevel() {
		return this.amountofSugar;
	}
	
	
	
	

}
